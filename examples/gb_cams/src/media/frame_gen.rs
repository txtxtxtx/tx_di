//! 视频帧生成器
//!
//! 从背景图片生成带时间戳和通道编码叠加的 RGB 帧。

use image::{Rgba, RgbaImage};

/// 生成一帧带叠加信息的图像
///
/// 在背景图上绘制：
/// - 左上角：当前时间（大字）
/// - 右下角：通道编码
///
/// 返回 RGBA8 像素数据
pub fn generate_frame(
    bg: &RgbaImage,
    _width: u32,
    _height: u32,
    timestamp: &str,
    channel_id: &str,
) -> Vec<u8> {
    // 创建帧缓冲（从背景图克隆）
    let mut frame = bg.clone();

    // 叠加时间文字（使用简单的像素绘制）
    draw_text_simple(&mut frame, 16, 16, timestamp, [255, 255, 255, 255]);
    // 叠加通道编码
    draw_text_simple(&mut frame, 16, 48, channel_id, [255, 255, 0, 255]);

    frame.into_raw()
}

/// 简易位图文字绘制器
///
/// 使用 5x7 像素字体，在图像上绘制 ASCII 字符。
/// 这是一个最小化的实现，无需外部字体文件。
fn draw_text_simple(
    img: &mut RgbaImage,
    x: u32,
    y: u32,
    text: &str,
    color: [u8; 4],
) {
    let scale: u32 = 2; // 每个像素放大 2 倍
    let char_width = 5 * scale + scale; // 字符间距
    let mut cursor_x = x;

    for ch in text.chars() {
        let bitmap = get_char_bitmap(ch);
        for row in 0u32..7 {
            for col in 0u32..5 {
                if bitmap[row as usize] & (1 << (4 - col)) != 0 {
                    // 绘制 scale x scale 的像素块
                    for dy in 0..scale {
                        for dx in 0..scale {
                            let px = cursor_x + col * scale + dx;
                            let py = y + row * scale + dy;
                            if px < img.width() && py < img.height() {
                                img.put_pixel(px, py, Rgba(color));
                            }
                        }
                    }
                }
            }
        }
        cursor_x += char_width;
    }
}

